#Faça um programa em python no qual o usuário informa um número inteiro e o programa exibe como saída seu antecessor e seu sucessor.

x=int(input("Digite um número inteiro para saber seu sucessor e seu antecessor:"))

antecessor=int(x-1)
sucessor=int(x+1)

print("O antessecor do número digitado é:",antecessor)
print("O sucessor do número digitado é:",sucessor)