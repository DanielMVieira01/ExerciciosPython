# Faça um programa em python que imprima a média aritmética de 4 números informados pelo usuário. Atente-se para a existência de números nos formatos inteiro e float. Faça o tratamento apropriado.

x=float(input("Digite o primero número:"))
y=float(input("Digite o segundo número:"))
z=float(input("Digite o terceiro número:"))
